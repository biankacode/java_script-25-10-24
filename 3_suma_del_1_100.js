/* Ejercicio 3: Sumar los primeros 100 números
Este ejercicio te ayudará a ver cómo un ciclo puede ser usado para realizar cálculos.
Objetivo: Usa un ciclo for para sumar todos los números del 1 al 100 y mostrar el resultado final. */
let suma = 0

for (let i = 1; i <= 5; i++ ){
   suma = suma + i; /* toma el valor actual de suma y agrégale el valor de i */
}
console.log("la suma de los primeros 5 numeros es : " + suma );
